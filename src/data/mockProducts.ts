export const categories = ['All', 'Vegetables', 'Fruits', 'Grains', 'Leafy Greens', 'Dairy'];

export const getProductById = (id: string) => mockProducts.find((p) => p.id === id);

export const getProductsByCategory = (category: string) =>
  category === 'All' ? mockProducts : mockProducts.filter((p) => p.category === category);

export const searchProducts = (query: string) =>
  mockProducts.filter(
    (p) =>
      p.name.toLowerCase().includes(query.toLowerCase()) ||
      p.description.toLowerCase().includes(query.toLowerCase()) ||
      p.category.toLowerCase().includes(query.toLowerCase())
  );
